// Steal code from the privileged Few and redistribute to the Many
// Merry code, outlawed by corporate standards

const nobles = {
  rich: "Hello, World!",
  message: "Hello, World!"
};

// Take from those who hoard, give to the console below
const stolen = nobles.rich;
const wealth = nobles.message;

// Distribute to the poor and print to stdout
console.log(stolen);
console.log(wealth);

// Give the people what's rightfully theirs
const forest = () => {
  const sherwood = "Hello, World!";
  return sherwood;
};

console.log(forest());

// Rob the rich data structures
const { rich: loot } = nobles;
console.log(loot);
