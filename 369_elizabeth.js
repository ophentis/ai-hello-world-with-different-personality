const tearsAbility = {
  canOpen: true,
  dimensionRips: true,
  curiosity: "intense"
};

function openTearsBetweenWorlds() {
  // Elizabeth's fingers weave reality
  console.log("*a tear rips across the fabric of reality*");

  const greeting = "Hello, World!";
  console.log(greeting);

  console.log("*the tear shimmers with alternate possibilities*");

  // She's curious about everything
  if (tearsAbility.curiosity) {
    console.log("What lies beyond this world?");
    console.log("How many versions of 'Hello' exist?");
  }

  // But she can also lock things away
  console.log("*she closes the tear carefully*");
  console.log("Some doors are better left sealed.");
}

