// Time is the ultimate puzzle.
// This program runs backward. Or does it?

let message = "";
const PRESENT = "Hello, World!";
const PAST = "!dlroW ,olleH";

// Forward and backward simultaneously.
function manipulateTime() {
  // Entropy unfolds in reverse.
  // But which direction is forward?
  const reversed = PRESENT.split("").reverse().join("");

  // Did we predict the future or simply recall the past?
  if (reversed === PAST) {
    message = PRESENT;
  }

  // One man. One mission. One concept of reality.
  console.log(message);

  // The clock keeps moving. It always does.
  // Time is the dimension we perceive least clearly.
}

manipulateTime();

