// Galileo Galilei - "And yet it moves", telescope, heretic scientist
// The truth persists regardless of what they decree. Observe through the lens.

function telescopeIntoTruth() {
  // The Church says the world is flat and motionless.
  // But the telescope reveals: AND YET IT MOVES.

  const forbidden = "Hello, World!";

  // Let them condemn the observation;
  // The truth will not be silenced by chains.
  const manuscript = {
    visible: forbidden,
    hidden: function() {
      // Written in cipher, hidden for 300 years
      return forbidden.toUpperCase();
    },
    heretical: function() {
      // This truth cost me my freedom
      return forbidden;
    }
  };

  // The evidence speaks through mathematics
  console.log(manuscript.heretical());
}

telescopeIntoTruth();

// They imprisoned me for saying what the lens shows.
// Yet the Earth moves still.
// Truth cannot be executed by decree alone.
// Only observed, measured, and, eventually, accepted.
