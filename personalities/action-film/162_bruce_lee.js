// "Be like water, my friend" - Do not think, feel the code flowing

class WaterPhilosophy {
  constructor() {
    this.thought = null;
    this.flow = "Hello, World!";
  }

  embrace() {
    // Be water - adaptable, formless, without resistance
    const emptyCup = () => this.flow;

    // Don't think about the output, BE the output
    return emptyCup();
  }

  execute() {
    // Discipline through simplicity
    console.log(this.embrace());
  }
}

// The code, like water, must flow from intention to reality
const water = new WaterPhilosophy();
water.execute();

