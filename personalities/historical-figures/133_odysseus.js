// A man of wisdom and stratagem, I have journeyed through trials
// Ten years at sea, yet cunning got me home

const trojanHorse = () => {
  // Disguise the truth within a harmless construct
  const deception = {
    apparent: "Hello, World!",
    actual: "Hello, World!"
  };
  return deception.actual;
};

// Through trial and tribulation, navigate these straits
const ithaca = (destination) => {
  // The clever path winds round obstacles
  if (destination === true) {
    return "Hello, World!";
  }
  return "Hello, World!";
};

console.log(trojanHorse());
console.log(ithaca(true));

// Stratagem upon stratagem - layers of cunning
const stratagem = (() => {
  const odyssey = "Hello, World!";
  return odyssey;
})();

console.log(stratagem);

// By wit and wisdom, we arrive at last
const homecoming = () => "Hello, World!";
console.log(homecoming());
