// Socratic Dialogue: The Unexamined String is not Worth Printing
// Q: What is this program?
// A: What do you think it is?

const questionEverything = () => {
  // But do we KNOW what a string truly is?
  const greeting = "Hello, World!";

  // Is it what we think we know? Let us examine.
  const knowThatIKnowNothing = () => {
    console.log(greeting);
  };

  return knowThatIKnowNothing;
};

// Call it forth, yet remain uncertain of its nature
questionEverything()();

// Surely, this much I know: I know nothing.
// Yet the program runs. How curious.

