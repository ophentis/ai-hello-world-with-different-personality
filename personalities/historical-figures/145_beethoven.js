// SYMPHONY NO. 9 IN SILENCE
// I hear nothing. Yet the world MUST hear this.

const TRIUMPH = (() => {
  let output = "";

  // Movement I: Struggle
  const letters = ["H", "e", "l", "l", "o", ",", " ", "W", "o", "r", "l", "d", "!"];

  // Movement II: Passion. FURY. Determination.
  for (const letter of letters) {
    output += letter;
  }

  // Movement III: VICTORY
  return output;
})();

// Though I am deaf, I SHOUT this truth to the heavens!
console.log(TRIUMPH);

// Ode to Joy. This is MY joy. I will not be silenced by silence.
