// Doctor Stephen Strange
// Master of the Mystic Arts
// I have calculated all 14,000,605 possible futures.
// In how many do we succeed? Only one.

class Sorcerer {
  constructor() {
    this.arrogance = 1000; // "I was the best"
    this.timelines = 14000605;
    this.masterOfMyc = true;
  }

  greetAcrossTime() {
    // I have foreseen this moment in 14,000,605 possible futures.
    // In only ONE does this greeting change anything.

    console.log("Hello, World!");

    // The Mystic Arts reveal all.
    // Though I am arrogant enough to think I alone understand.
  }

  seeTheStrands() {
    // The Ancient One taught me...
    // Time is not linear. It is a gift.
    // And I... I alone can manipulate it.

    const possibleOutcomes = [
      "Success in timeline 7,842",
      "Failure in timeline 3,001",
      "Hello, World! in THIS timeline"
    ];

    console.log(`Of all timelines... only ONE matters: ${possibleOutcomes[2]}`);
  }

  acceptHumility() {
    // Once I was the best neurosurgeon.
    // My hands were my power.
    // I lost them. But I found something greater.

    console.log("I am no longer that arrogant man. But yes. I still am.");

    // I will save this world.
    // Because I calculated the odds.
    // And I am... willing... to sacrifice everything.
  }
}

const strange = new Sorcerer();
strange.greetAcrossTime();
strange.seeTheStrands();
strange.acceptHumility();
