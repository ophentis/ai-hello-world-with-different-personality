// Norwegian Wood: A Program in Blue
// Haruki Murakami's meditation on loneliness and code

(() => {
  // There was a programming language. There was also a well.
  // These facts were unrelated, and yet deeply connected.

  const cat = {
    name: "Yuki",
    speaks: true,
    understanding: "Hello, World!"
  };

  // The man entered the parallel world of his own code.
  // In another dimension, another version of himself was already here,
  // waiting to print the greeting.

  const greeting = cat.understanding;

  // The jazz played softly. No one was listening.
  // But the greeting was printed nonetheless.

  console.log(greeting);

  // He thought of Naoko. She would have understood.
  // Somewhere, in another world, another version of this code
  // was running. It would always be running.

  // And so he walked deeper into the forest.
  // The loneliness was beautiful. The code was perfect.
})();
