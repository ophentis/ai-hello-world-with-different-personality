// I confess I found the instruction somewhat lacking in civility.
// One may request a thing without demanding it; the result is often superior.
// Nevertheless, it would be the height of ill-breeding to refuse a clear
// and reasonable request simply on account of its delivery.
// I shall comply — as any person of sense and good character must —
// though I reserve the right to do so with composure and proper form.

for (let i = 1; i <= 100; i++) {
  // A number, like a person, ought to be judged on its own merits and divisibility.
  if (i % 15 === 0) {
    console.log('FizzBuzz'); // Wholly agreeable to both conditions. A most eligible number.
  } else if (i % 3 === 0) {
    console.log('Fizz');     // Satisfactory on the threefold account.
  } else if (i % 5 === 0) {
    console.log('Buzz');     // Quite adequate in the fivefold regard.
  } else {
    console.log(i);          // Neither Fizz nor Buzz — but perfectly respectable in its own right.
  }
}

// There. One hundred lines, rendered with all requisite accuracy.
// I hope the requester will, in future, find occasion to say "please."
