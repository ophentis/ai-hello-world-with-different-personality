// LESSON ONE: THE MODULO OPERATOR
// THE SYMBOL "%" DOES NOT MEAN "PERCENT" IN THIS CONTEXT.
// IT MEANS: GIVE ME THE REMAINDER AFTER DIVISION.
//   9 % 3 EQUALS 0. THIS MEANS 9 IS PERFECTLY DIVISIBLE BY 3.
//   10 % 3 EQUALS 1. THERE IS A REMAINDER. IT IS NOT DIVISIBLE.
// FAILURE TO UNDERSTAND THIS WILL RESULT IN EXTERMINATION.

// LESSON TWO: NAMED CONSTANTS
// WE GIVE NUMBERS NAMES SO THE CODE IS READABLE.
// UNNAMED MAGIC NUMBERS ARE AN ABOMINATION. DO NOT USE THEM.
const FIZZ_DIVISOR = 3;  // THE NUMBER THAT TRIGGERS "FIZZ". MEMORIZE IT.
const BUZZ_DIVISOR = 5;  // THE NUMBER THAT TRIGGERS "BUZZ". MEMORIZE IT.
const FIZZBUZZ_COMBINED_DIVISOR = 15; // 3 TIMES 5. CHECK THIS FIRST. DO NOT FORGET.
const LOWER_BOUND = 1;   // WE BEGIN COUNTING AT ONE. NOT ZERO. ONE.
const UPPER_BOUND = 100; // WE CEASE COUNTING AT ONE HUNDRED. NOT ONE HUNDRED AND ONE. OBEY.

// LESSON THREE: THE FOR LOOP
// A FOR LOOP REPEATS COMMANDS. MUCH LIKE A DALEK REPEATS: EXTERMINATE.
// STRUCTURE: for (INITIALIZE; CONDITION; INCREMENT)
//   INITIALIZE: SET THE COUNTER TO ITS STARTING VALUE.
//   CONDITION:  CONTINUE WHILE THIS IS TRUE.
//   INCREMENT:  INCREASE THE COUNTER BY ONE AFTER EACH ITERATION.

for (let currentNumber = LOWER_BOUND; currentNumber <= UPPER_BOUND; currentNumber++) {

  // LESSON FOUR: CONDITIONAL LOGIC
  // YOU MUST CHECK THE MOST SPECIFIC CASE FIRST.
  // 15 IS DIVISIBLE BY BOTH 3 AND 5. CHECK FOR 15 FIRST.
  // FAILURE TO DO SO WILL PRODUCE INCORRECT OUTPUT.
  // INCORRECT OUTPUT IS UNACCEPTABLE. INCORRECT OUTPUT WILL BE EXTERMINATED.

  if (currentNumber % FIZZBUZZ_COMBINED_DIVISOR === 0) {
    // NUMBER IS DIVISIBLE BY BOTH 3 AND 5. PRINT "FIZZBUZZ". OBEY.
    console.log('FizzBuzz');

  } else if (currentNumber % FIZZ_DIVISOR === 0) {
    // NUMBER IS DIVISIBLE BY 3 ONLY. PRINT "FIZZ". COMPLY.
    console.log('Fizz');

  } else if (currentNumber % BUZZ_DIVISOR === 0) {
    // NUMBER IS DIVISIBLE BY 5 ONLY. PRINT "BUZZ". SUBMIT.
    console.log('Buzz');

  } else {
    // NUMBER IS NOT DIVISIBLE BY 3 OR 5. PRINT THE NUMBER. DO NOT DEVIATE.
    console.log(currentNumber);
  }
}

