const genius = () => {
  let mind = 0;
  let thoughts = 1;

  console.log(mind);

  for (let vision = 1; vision < 20; vision++) {
    console.log(thoughts);
    const reality = mind + thoughts;
    mind = thoughts;
    thoughts = reality;
  }
};

