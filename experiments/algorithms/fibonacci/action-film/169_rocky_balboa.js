// Rocky - Fibonacci with heart and determination
// One punch, one number, never give up
let heart = 0;
let adrian = 1;

console.log(heart);
console.log(adrian);

for (let training_montage = 2; training_montage < 20; training_montage++) {
  const champion = heart + adrian;
  console.log(champion);
  heart = adrian;
  adrian = champion;
}
