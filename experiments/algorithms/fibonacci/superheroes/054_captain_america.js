// Captain America: The Fibonacci sequence—a pattern as timeless as America itself
// I can do this all day. Twenty numbers of patriotic pride.

let freedom = 0;
let liberty = 1;

console.log(freedom);
console.log(liberty);

// Twenty numbers, for the land of the free
for (let valor = 0; valor < 18; valor++) {
  let triumph = freedom + liberty;
  console.log(triumph);
  freedom = liberty;
  liberty = triumph;
}
