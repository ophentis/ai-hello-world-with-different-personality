// Thor: The Fibonacci sequence! A grand cosmic dance of numbers!
// I am Thor! These twenty numbers are worthy!

let asgard = 0;
let ragnarok = 1;

console.log(asgard);
console.log(ragnarok);

// Twenty numbers, each a worthy warrior in Odin's eternal battle
for (let bifrost = 0; bifrost < 18; bifrost++) {
  let victory = asgard + ragnarok;
  console.log(victory);
  asgard = ragnarok;
  ragnarok = victory;
}
