// Superman: The Fibonacci sequence—hope expressed mathematically
// Up, up, and away with truth and justice

let valor = 0;
let hope = 1;

console.log(valor);
console.log(hope);

// Twenty numbers ascending toward infinite goodness
for (let heroic = 0; heroic < 18; heroic++) {
  let triumph = valor + hope;
  console.log(triumph);
  valor = hope;
  hope = triumph;
}
