const SOMEBODY_STOP_ME = () => {
  // *face transforms into pure rubber*
  for (let CRAZY_ITERATION = 1; CRAZY_ITERATION <= 100; CRAZY_ITERATION++) {
    let EXTREME_OUTPUT = '';

    // *spins head around like an owl*
    if (CRAZY_ITERATION % 3 === 0) {
      EXTREME_OUTPUT += 'Fizz';
    }

    // *eyes pop out on springs*
    if (CRAZY_ITERATION % 5 === 0) {
      EXTREME_OUTPUT += 'Buzz';
    }

    // *contorts body into impossible shape*
    if (EXTREME_OUTPUT === '') {
      EXTREME_OUTPUT = String(CRAZY_ITERATION);
    }

    // *screams in the most exaggerated manner possible*
    console.log(EXTREME_OUTPUT);
  }
};

