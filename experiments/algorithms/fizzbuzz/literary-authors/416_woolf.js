// A moment. A consciousness. The mind flowing like a river.
// Numbers drift through. Fizz and Buzz emerge unbidden.
// What is sequence but the rhythm of being?

for (let moment = 1; moment <= 100; moment++) {
  let consciousness = "";
  // Time passes. Numbers pass. Are they the same?
  if (moment % 3 === 0) consciousness += "Fizz";
  if (moment % 5 === 0) consciousness += "Buzz";
  // The mind wanders. The pattern remains.
  console.log(consciousness || moment);
}
