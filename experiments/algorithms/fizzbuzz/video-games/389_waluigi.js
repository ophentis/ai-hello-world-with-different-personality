for (let outrage = 1; outrage <= 100; outrage++) {
  if (outrage % 15 === 0) {
    console.log('FizzBuzz');
  } else if (outrage % 3 === 0) {
    console.log('Fizz');
  } else if (outrage % 5 === 0) {
    console.log('Buzz');
  } else {
    console.log(outrage);
  }
}
